let CURRENT_LANG = localStorage.getItem("sriSivaLang") || "en";

function t(key) {
  return TRANSLATIONS[CURRENT_LANG]?.[key] || TRANSLATIONS.en[key] || key;
}

function toggleLangDropdown() {
  const menu = document.getElementById("langDropdownMenu");
  if (menu) {
    menu.classList.toggle("show");
  }
}

function setLanguage(lang) {
  CURRENT_LANG = lang;
  localStorage.setItem("sriSivaLang", CURRENT_LANG);
  document.documentElement.lang = CURRENT_LANG;
  updatePageLanguage();
  closeLangDropdown();
}

function closeLangDropdown() {
  const menu = document.getElementById("langDropdownMenu");
  if (menu) {
    menu.classList.remove("show");
  }
}

function updatePageLanguage() {
  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const translated = t(key);
    el.textContent = translated;
  });
  
  // Update all placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    const translated = t(key);
    el.placeholder = translated;
  });
  
  // Update language display
  const langDisplay = document.getElementById("langDisplay");
  if (langDisplay) {
    langDisplay.textContent = CURRENT_LANG === "en" ? "English" : "தமிழ்";
  }
  
  // Update checkmarks for language options
  document.querySelectorAll(".lang-option").forEach(btn => {
    const btnLang = btn.getAttribute("data-lang");
    btn.classList.toggle("active", btnLang === CURRENT_LANG);
  });
  
  console.log("Language updated to:", CURRENT_LANG);
}

// Close dropdown when clicking outside
document.addEventListener("click", (e) => {
  const wrapper = document.querySelector(".lang-dropdown-wrapper");
  if (wrapper && !wrapper.contains(e.target)) {
    closeLangDropdown();
  }
});

// Initialize on page load
document.addEventListener("DOMContentLoaded", () => {
  document.documentElement.lang = CURRENT_LANG;
  updatePageLanguage();
  console.log("Language switcher initialized. Current language:", CURRENT_LANG);
});
